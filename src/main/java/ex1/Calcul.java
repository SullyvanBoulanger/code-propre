package ex1;

public class Calcul {

	/**
	 * Sum the 2 integer parameters and return the result
	 * @param a Integer
	 * @param b Integer
	 * @return Result of the sum
	 */
	public int sum(int a, int b){
		return a+b;
	}
	
	/**
	 * Substract b from a and return the result
	 * @param a Integer
	 * @param b Integer
	 * @return Result of the substract
	 */
	public int subtract(int a, int b){
		return a-b;
	}
}
